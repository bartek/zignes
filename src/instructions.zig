pub const Operation = struct {
    cycles: i8 = 0,
};

pub fn operation(cpu: *c.CPU, opcode: u8) Operation {
    switch (opcode) {
        0x0 => {}, // NOOP
        0xaa => {
            cpu.X = cpu.A;
            cpu.setZN(cpu.X);
        },
        else => {
            panic("\n!! not implemented 0x{x}\n", .{opcode});
        },
    }

    return Operation{
        .cycles = OP_CYCLES[opcode],
    };
}
